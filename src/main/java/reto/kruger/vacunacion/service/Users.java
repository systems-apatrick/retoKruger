package reto.kruger.vacunacion.service;

public class Users {
    private String id_user ;
    private String name_user ;
    private String lastname_user ;
    private String email ;
    private String login_user ;
    private String pass_user  ;
    private String code_rol ;
    private String editor_user  ;
    private String birth_date_user ;
    private String address_user;
    private String phone_number_user  ;

    public String getId_user() {
        return id_user;
    }

    public void setId_user(String id_user) {
        this.id_user = id_user;
    }

    public String getName_user() {
        return name_user;
    }

    public void setName_user(String name_user) {
        this.name_user = name_user;
    }

    public String getLastname_user() {
        return lastname_user;
    }

    public void setLastname_user(String lastname_user) {
        this.lastname_user = lastname_user;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getLogin_user() {
        return login_user;
    }

    public void setLogin_user(String login_user) {
        this.login_user = login_user;
    }

    public String getPass_user() {
        return pass_user;
    }

    public void setPass_user(String pass_user) {
        this.pass_user = pass_user;
    }

    public String getCode_rol() {
        return code_rol;
    }

    public void setCode_rol(String code_rol) {
        this.code_rol = code_rol;
    }

    public String getEditor_user() {
        return editor_user;
    }

    public void setEditor_user(String editor_user) {
        this.editor_user = editor_user;
    }

    public String getBirth_date_user() {
        return birth_date_user;
    }

    public void setBirth_date_user(String birth_date_user) {
        this.birth_date_user = birth_date_user;
    }

    public String getAddress_user() {
        return address_user;
    }

    public void setAddress_user(String address_user) {
        this.address_user = address_user;
    }

    public String getPhone_number_user() {
        return phone_number_user;
    }

    public void setPhone_number_user(String phone_number_user) {
        this.phone_number_user = phone_number_user;
    }
}
